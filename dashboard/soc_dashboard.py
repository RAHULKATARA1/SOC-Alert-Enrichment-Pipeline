with tab1:
    st.subheader("Prioritized Alert Queue")
    if not df_alerts_filtered.empty:
        # Sort by score descending
        st.dataframe(
            df_alerts_filtered.sort_values(by='Score', ascending=False),
            use_container_width=True,
            column_config={
                "Score": st.column_config.ProgressColumn(
                    "Risk Score", min_value=0, max_value=100, format="%f"
                )
            }
        )
        
with tab2:
    st.subheader("Threat Intelligence Matches")
    if not df_iocs.empty:
        # Filter to only malicious IOCs
        malicious_df = df_iocs[df_iocs['Malicious'] == True].sort_values(by='Abuse Confidence', ascending=False)
        st.dataframe(malicious_df, use_container_width=True)
    else:
        st.write("No IOCs found.")
        
with tab3:
    st.subheader("Automated Response Audit Log")
    if not df_actions.empty:
        st.dataframe(df_actions, use_container_width=True)
    else:
        st.write("No automated actions taken.")

